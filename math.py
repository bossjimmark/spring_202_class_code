# take two values input by the user
# do arithmetic operations on them

# get the values
first_number=int(input("Enter a number: "))
second_number=int(input("Enter a second number: "))

# do the math
summed = first_number + second_number
diff = first_number - second_number
product = first_number * second_number
quotient = first_number / second_number
remainder = first_number % second_number
power_val = first_number ** second_number

print(summed)
print(diff)
print(product)
print(quotient)
print(remainder)
print(power_val)